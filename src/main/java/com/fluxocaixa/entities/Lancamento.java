package com.fluxocaixa.entities;

import com.fluxocaixa.enums.TipoLancamento;
import lombok.AllArgsConstructor;
import lombok.NoArgsConstructor;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

import java.math.BigDecimal;
import java.time.LocalDateTime;

@Document(collection = "lancamentos")
@AllArgsConstructor
@NoArgsConstructor
public class Lancamento {

    @Id
    private String id;
    private String descricao;
    private double valor;
    private TipoLancamento tipo;
    private LocalDateTime dateTime;

    private String idUsuario;

    private BigDecimal consolidado;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public TipoLancamento getTipo() {
        return tipo;
    }

    public void setTipo(TipoLancamento tipo) {
        this.tipo = tipo;
    }

    public LocalDateTime getDateTime() {
        return dateTime;
    }

    public void setDateTime(LocalDateTime dateTime) {
        this.dateTime = dateTime;
    }

    public BigDecimal getConsolidado() {
        return consolidado;
    }

    public void setConsolidado(BigDecimal consolidado) {
        this.consolidado = consolidado;
    }

    public String getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(String idUsuario) {
        this.idUsuario = idUsuario;
    }
}
